let palavra = prompt("Insira uma palavra");

let palavraMinuscula = palavra.toLocaleLowerCase();
let palavraMaiuscula = palavra.toLocaleUpperCase();

alert(`A palavra que voce digitou é ${palavra} em letras minusculas é ${palavraMinuscula}`);

alert(`A palavra que voce digitou é ${palavra} em letras maiusculas é ${palavraMaiuscula}`);
